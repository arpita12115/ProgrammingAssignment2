## The below pair of functions work to cache the inverse of a matrix to prevent it from being computed repeatedly. 
## makeCacheMatrix is used to create a special matrix whose inverse is calculated in the cacheSolve function only
## if the inverse has not already been calculated or the matrix has changed. If the inverse has already been
## calculated or if the matrix remains unchanged, the cacheSolve function will retrive the inverse from the cache

## This function creates a special "matrix" object that can cache its inverse

makeCacheMatrix <- function(x = matrix()) {
  inv <- NULL
  set <- function(y) {
    x <<- y
    inv <<- NULL
  }
  get <- function() x
  setinverse <- function(inverse) inv <<- inverse
  getinverse <- function() inv
  list(set = set,
       get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}

